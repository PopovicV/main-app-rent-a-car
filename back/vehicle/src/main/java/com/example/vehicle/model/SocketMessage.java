package com.example.vehicle.model;

public class SocketMessage {
    private String lat;
    private String lon;

    public SocketMessage() {
    }

    public String getLat() {
        return lat;
    }

    public void setLat(String lat) {
        this.lat = lat;
    }

    public String getLon() {
        return lon;
    }

    public void setLon(String lon) {
        this.lon = lon;
    }
}
